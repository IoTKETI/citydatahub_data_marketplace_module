package kr.co.n2m.smartcity.admin.common.interceptor;

import javax.servlet.http.HttpServletRequest;
import javax.servlet.http.HttpServletResponse;

import org.springframework.beans.factory.annotation.Autowired;
import org.springframework.stereotype.Component;
import org.springframework.web.servlet.ModelAndView;
import org.springframework.web.servlet.handler.HandlerInterceptorAdapter;

import kr.co.n2m.smartcity.admin.common.component.AuthComponent;
import kr.co.n2m.smartcity.admin.common.exceptions.AuthException;
import kr.co.n2m.smartcity.admin.common.utils.StringUtil;

@Component
public class HandlerInterceptor extends HandlerInterceptorAdapter {

	@Autowired
	private AuthComponent authComponent;
	
	@Override
	public boolean preHandle(HttpServletRequest request, HttpServletResponse response, Object handler)
			throws Exception {
		
		if ( request.getRequestURI().indexOf("/error") == -1 ) {
	//		if(request.getRequestURI().toLowerCase().contains("null")) {
	//			throw new ClientException("요청 한 URI 정보가 잘못 되었습니다 : NULL 포함");
	//		}
				
			String token = ((HttpServletRequest) request).getHeader("Authorization");
			if (StringUtil.isNotEmpty(token) && !authComponent.validateToken(token)) {
				throw new AuthException("It's wrong authentication or authorization");
			}
		}
		return super.preHandle(request, response, handler);
	}
	
	@Override
	public void postHandle(HttpServletRequest request, HttpServletResponse response, Object handler,
			ModelAndView modelAndView) throws Exception {
		super.postHandle(request, response, handler, modelAndView);
	}

	
	@Override
	public void afterCompletion(HttpServletRequest request, HttpServletResponse response, Object handler, Exception ex)
			throws Exception {
		super.afterCompletion(request, response, handler, ex);
	}
	
}
