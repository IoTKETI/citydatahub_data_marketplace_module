package kr.co.smartcity.admin.common.entity;

public class UserInfo {
	private String userId;
	private String userNm;
	private String name;
	private String phone;
	private String email;

	public UserInfo() {}
	public UserInfo(String userId, String userNm, String name, String phone, String email) {
		this.userId = userId;
		this.userNm = userNm;
		this.name = name;
		this.phone = phone;
		this.email = email;
	}

	public String getUserId() {
		return userId;
	}

	public void setUserId(String userId) {
		this.userId = userId;
	}

	public String getUserNm() {
		return userNm;
	}

	public void setUserNm(String userNm) {
		this.userNm = userNm;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getPhone() {
		return phone;
	}

	public void setPhone(String phone) {
		this.phone = phone;
	}

	public String getEmail() {
		return email;
	}

	public void setEmail(String email) {
		this.email = email;
	}

	@Override
	public String toString() {
		return "UserInfo [userId=" + userId + ", userNm=" + userNm + ", name=" + name + ", phone=" + phone + ", email="
				+ email + "]";
	}
	
	
}
